  def mkdh(key_agr) do
    p =
      case key_agr do
        @zrtp_key_agreement_dh2k -> ret_P2048()
        @zrtp_key_agreement_dh3k -> ret_P3072()
        @zrtp_key_agreement_dh4k -> ret_P4096()
      end

    g = :crypto.mpint(2)
    :crypto.dh_generate_key([p, g])
  end

  def mkfinal(pvr, private_key) do
    g = :crypto.mpint(2)

    case byte_size(pvr) do
      256 ->
        p = ret_P2048()
        :crypto.dh_compute_key(<<256::integer-big-size(32), pvr::binary>>, private_key, [p, g])

      384 ->
        p = ret_P3072()
        :crypto.dh_compute_key(<<384::integer-big-size(32), pvr::binary>>, private_key, [p, g])

      512 ->
        p = ret_P4096()
        :crypto.dh_compute_key(<<512::integer-big-size(32), pvr::binary>>, private_key, [p, g])
    end
  end

  def kdf(@zrtp_hash_s256, key, label, kdf_context),
    do:
      crypto_mac_sha256(
        key,
        <<1::size(32), label::binary, 0::size(32), kdf_context::binary, 256::size(8)>>
      )

  def kdf(@zrtp_hash_s384, key, label, kdf_context),
    do:
      crypto_mac_sha384(
        key,
        <<1::size(32), label::binary, 0::size(32), kdf_context::binary, 384::size(8)>>
      )

  def sas(sas_value, @zrtp_sas_type_b32), do: :sas.b32(sas_value)
  def sas(sas_value, @zrtp_sas_type_b256), do: :sas.b256(sas_value)

  def get_hashfun(@zrtp_hash_s256), do: &crypto_hash_sha256/1
  def get_hashfun(@zrtp_hash_s384), do: &crypto_hash_sha384/1
  def get_hmacfun(@zrtp_hash_s256), do: &crypto_mac_sha256/2
  def get_hmacfun(@zrtp_hash_s384), do: &crypto_mac_sha384/2

  def get_hashlength(@zrtp_hash_s256), do: 32
  def get_hashlength(@zrtp_hash_s384), do: 48

  def get_keylength(@zrtp_cipher_aes1), do: 16
  def get_keylength(@zrtp_cipher_aes2), do: 24
  def get_keylength(@zrtp_cipher_aes3), do: 32
  def get_taglength(@zrtp_auth_tag_hs32), do: 4
  def get_taglength(@zrtp_auth_tag_hs80), do: 10
  def get_taglength(@zrtp_auth_tag_sk32), do: 4
  def get_taglength(@zrtp_auth_tag_sk64), do: 8

  def mkhmac(msg, hash) do
    payload = Zrtp.encode_message(msg)
    size = byte_size(payload) - 8
    <<data::binary-size(size), _::binary>> = payload
    <<mac::binary-size(8), _::binary>> = crypto_mac_sha256(hash, data)
    mac
  end

  def verify_hmac(_, _, nil), do: false
  def verify_hmac(msg, mac, hash), do: mac == mkhmac(msg, hash)

  def crypto_hash_sha256(data), do: :crypto.hash(:sha256, data)
  def crypto_hash_sha384(data), do: :crypto.hash(:sha384, data)

  def crypto_mac_sha256(hash, data), do: :crypto.sha256_mac(hash, data)
  def crypto_mac_sha384(hash, data), do: :crypto.sha384_mac(hash, data)
end
